const T_CACHE = {};

export const getT = (lang) => {
  const key = String(lang || '').trim().toLowerCase();
  if (T_CACHE[key]) return T_CACHE[key];

  const current = TRANSLATIONS[key] || {};
  const merged = { ...TRANSLATIONS.pt, ...current };
  T_CACHE[key] = merged;
  return merged;
};

export const getLocaleForLang = (lang) => {
  const key = String(lang || '').trim().toLowerCase();
  if (key === 'en') return 'en-US';
  if (key === 'es') return 'es-ES';
  return 'pt-BR';
};

export const formatMoneyUsd = (amount, lang) => {
  const n = Number(amount || 0);
  try {
    return new Intl.NumberFormat(getLocaleForLang(lang), {
      style: 'currency',
      currency: 'USD',
      minimumFractionDigits: 2,
      maximumFractionDigits: 2,
    }).format(n);
  } catch {
    return `$${n.toFixed(2)}`;
  }
};

export const formatMoneyUsdInt = (amount, lang) => {
  const n = Number(amount || 0);
  try {
    return new Intl.NumberFormat(getLocaleForLang(lang), {
      style: 'currency',
      currency: 'USD',
      minimumFractionDigits: 0,
      maximumFractionDigits: 0,
    }).format(n);
  } catch {
    return `$${Math.round(n)}`;
  }
};

export const formatDateTime = (iso, lang) => {
  try {
    return new Intl.DateTimeFormat(getLocaleForLang(lang), { dateStyle: 'short', timeStyle: 'short' }).format(new Date(iso));
  } catch {
    return String(iso || '');
  }
};

export const formatDateShort = (iso, lang) => {
  try {
    return new Intl.DateTimeFormat(getLocaleForLang(lang), { dateStyle: 'short' }).format(new Date(iso));
  } catch {
    return String(iso || '');
  }
};

export const fillTemplate = (template, values) =>
  String(template || '').replace(/\{(\w+)\}/g, (_, key) => (Object.prototype.hasOwnProperty.call(values || {}, key) ? values[key] : ''));

export const getStatusLabel = (status, t) => {
  const value = String(status || '').trim().toLowerCase();
  if (value === 'aguardando depósito') return t.statusAwaitingPayment;
  if (value === 'pendente') return t.statusPending;
  if (value === 'concluído' || value === 'confirmado') return t.statusCompleted;
  if (value === 'creditado') return t.statusCredited;
  if (value === 'solicitado') return t.statusRequested;
  if (value === 'aprovado') return t.statusApproved;
  if (value === 'recusado') return t.statusRejected;
  if (value === 'pago') return t.statusPaid;
  return String(status || '—');
};

const normalizeTextKey = (value) =>
  String(value || '')
    .trim()
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '');

export const translateRankTitle = (rankTitle, t) => {
  const v = normalizeTextKey(rankTitle);
  if (!v) return String(rankTitle || '—');
  if (v === 'ferro' || v === 'iron') return t.rankIron;
  if (v === 'bronze') return t.rankBronze;
  if (v === 'silver' || v === 'prata') return t.rankSilver;
  if (v === 'ouro' || v === 'gold') return t.rankGold;
  if (v === 'diamond' || v === 'diamante') return t.rankDiamond;
  if (v === 'diamond rm' || v === 'diamondrm' || v === 'diamante rm' || v === 'diamanterm') return t.rankDiamondRm;
  return String(rankTitle || '—');
};

export const translateTransactionType = (type, t) => {
  const raw = String(type || '').trim();
  if (!raw) return raw;

  {
    const match = raw.match(/^Compra\s+(.+)$/i);
    if (match) return fillTemplate(t.txPurchaseTemplate, { plan: match[1] });
  }
  {
    const match = raw.match(/^Dep[oó]sito em processamento\s*•\s*(.+)$/i);
    if (match) return fillTemplate(t.txDepositProcessingTemplate, { plan: match[1] });
  }
  {
    const match = raw.match(/^Ganho de Rede \(TE\)\s*-\s*N[ií]vel\s*(\d+)\s*-\s*Compra\s*(.+)$/i);
    if (match) return fillTemplate(t.txTeamEarningsTemplate, { level: match[1], buyer: match[2] });
  }
  {
    const match = raw.match(/^Residual di[aá]rio\s*\((.+)\)$/i);
    if (match) return fillTemplate(t.txResidualDailyTemplate, { rank: translateRankTitle(match[1], t) });
  }

  return raw;
};

export const translateNotification = (notification, t, lang) => {
  const n = notification || {};
  if (n?.i18n?.titleKey || n?.i18n?.messageKey) {
    const title = t?.[n.i18n.titleKey] || n.title || '';
    const template = t?.[n.i18n.messageKey] || n.message || '';
    const rawValues = n.i18n.values || {};
    const values = { ...rawValues };
    if (Object.prototype.hasOwnProperty.call(values, 'rank')) values.rank = translateRankTitle(values.rank, t);
    if (lang && Object.prototype.hasOwnProperty.call(values, 'amount') && typeof values.amount === 'number') {
      values.amount = formatMoneyUsd(values.amount, lang);
    }
    return {
      ...n,
      title,
      message: fillTemplate(template, values),
    };
  }
  if (n.kind === 'RANK_UP') {
    const match = String(n.message || '').match(/^Parab[eé]ns!\s*Voc[eê]\s*alcan[cç]ou\s*o\s*rank\s*(.+)\.$/i);
    return {
      ...n,
      title: t.rankUpTitle,
      message: fillTemplate(t.rankUpMessageTemplate, { rank: translateRankTitle(match?.[1] || '', t) }),
    };
  }
  if (n.kind === 'RESIDUAL') {
    const match = String(n.message || '').match(/^Residual do dia creditado:\s*(.+)\s*\((.+)\)\.$/i);
    return {
      ...n,
      title: t.residualCreditedTitle,
      message: fillTemplate(t.residualCreditedMessageTemplate, {
        amount: match?.[1] || '',
        rank: translateRankTitle(match?.[2] || '', t),
      }),
    };
  }
  if (n.kind === 'TE') {
    const match = String(n.message || '').match(/^Voc[eê]\s*recebeu\s*(.+)\s*no n[ií]vel\s*(\d+)\s*\(compra de\s*(.+)\)\.$/i);
    return {
      ...n,
      title: t.notifTeamEarningsTitle,
      message: fillTemplate(t.notifTeamEarningsMessageTemplate, {
        amount: match?.[1] || '',
        level: match?.[2] || '',
        buyer: match?.[3] || '',
      }),
    };
  }
  if (n.kind === 'ELITE') {
    const match = String(n.message || '').match(/^Voc[eê]\s*recebeu\s*(.+)\s*USD\s*\((.+)\)\.$/i);
    return {
      ...n,
      title: t.bonusEliteCreditedTitle,
      message: fillTemplate(t.eliteCreditedMessageTemplate, {
        amount: match?.[1] || '',
        cat: match?.[2] || '',
      }),
    };
  }
  if (n.kind === 'RENEW') {
    const match = String(n.message || '').match(/^(.*): renovação iniciada \((.*)\)\.$/i);
    return {
      ...n,
      title: t.renewNotificationTitle,
      message: fillTemplate(t.renewNotificationMessage, {
        plan: match?.[1] || '',
        payment: match?.[2] || '',
      }),
    };
  }
  if (n.kind === 'DESIST_REQ') {
    const match = String(n.message || '').match(/^(.*): análise de (\d+)h\. Valor estimado: ([\d.]+) \(penalidade ([\d.]+)%\)\.$/i);
    return {
      ...n,
      title: t.desistanceNotificationTitle,
      message: fillTemplate(t.desistanceNotificationMessage, {
        plan: match?.[1] || '',
        hours: match?.[2] || '',
        amount: match?.[3] || '',
        penalty: match?.[4] || '',
      }),
    };
  }
  if (n.kind === 'CYCLE_DONE') {
    const match = String(n.message || '').match(/^(.*): liberado ([\d.]+) \((\d+)% taxa de entrada\)\. Renovação disponível por (\d+)h\.$/i);
    return {
      ...n,
      title: t.cycleDoneTitle,
      message: fillTemplate(t.cycleDoneMessageTemplate, {
        plan: match?.[1] || '',
        amount: match?.[2] || '',
        entryFeePct: match?.[3] || '',
        hours: match?.[4] || '',
      }),
    };
  }
  if (n.kind === 'QUOTAS_RELEASED') {
    const match = String(n.message || '').match(/^(.*): prazo de renovação expirou\./i);
    return {
      ...n,
      title: t.quotasReleasedTitle,
      message: fillTemplate(t.quotasReleasedMessageTemplate, {
        plan: match?.[1] || '',
      }),
    };
  }
  if (n.kind === 'DESIST_DONE') {
    const match = String(n.message || '').match(/^(.*): ressarcimento ([\d.]+) \(penalidade ([\d.]+)%\)\.$/i);
    return {
      ...n,
      title: t.desistanceDoneTitle,
      message: fillTemplate(t.desistanceDoneMessageTemplate, {
        plan: match?.[1] || '',
        amount: match?.[2] || '',
        penalty: match?.[3] || '',
      }),
    };
  }
  return n;
};

export const translateFinancialReason = (reason, t) => {
  const text = String(reason || '').trim();
  if (!text) return text;
  const userLimitMatch = text.match(/^Limite de (\d+) unidades por usuário atingido\.$/i);
  if (userLimitMatch) return fillTemplate(t.validationUserLimit, { count: userLimitMatch[1] });
  if (text === 'Limite global de 100.000 cotas atingido.') return t.validationGlobalLimit;
  if (text === 'Usuário bloqueado.') return t.withdrawBlockedReason;
  if (text === 'Valor mínimo para saque é $10.') return t.withdrawMinReason;
  if (text === 'Valor de saque insuficiente após taxa fixa.') return t.withdrawNetReason;
  if (text === 'Saldo disponível insuficiente.') return t.withdrawBalanceReason;
  if (text === 'Carteira de recebimento não configurada.') return t.withdrawWalletReason;
  if (text === 'Depósito não encontrado.') return t.depositNotFoundReason;
  if (text === 'Transação não é depósito.') return t.depositInvalidReason;
  if (text === 'Lote não encontrado.') return t.lotNotFoundReason;
  if (text === 'Este lote não está disponível para renovação.') return t.renewUnavailableReason;
  if (text === 'Prazo de renovação expirou.') return t.renewExpiredReason;
  if (text === 'Saldo disponível insuficiente para renovar.') return t.renewBalanceReason;
  if (text === 'Somente cotas ativas podem solicitar desistência.') return t.desistanceActiveOnlyReason;
  return text;
};

